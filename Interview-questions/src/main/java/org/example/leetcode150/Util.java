package org.example.leetcode150;

import java.util.List;

public class Util {

    public static void print(int[] arr){
        for(int i: arr){
            System.out.print(i+", ");
        }
        System.out.println();
    }

    public static void printList(List<Integer> arr){
        for(int i: arr){
            System.out.print(i+", ");
        }
        System.out.println();
    }
}
